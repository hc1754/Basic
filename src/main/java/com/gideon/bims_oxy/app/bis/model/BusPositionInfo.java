package com.gideon.bims_oxy.app.bis.model;

import java.math.BigDecimal;

public class BusPositionInfo {
	
	private String busId;
	private String busNumber;
	private String lastBusStopId;
	private BigDecimal distanceRate;
	private BigDecimal correctLat;
	private BigDecimal correctLon;
	

	public String getBusId() {
		return busId;
	}

	public void setBusId(String busId) {
		this.busId = busId;
	}

	public String getBusNumber() {
		return busNumber;
	}

	public void setBusNumber(String busNumber) {
		this.busNumber = busNumber;
	}

	public String getLastBusStopId() {
		return lastBusStopId;
	}

	public void setLastBusStopId(String lastBusStopId) {
		this.lastBusStopId = lastBusStopId;
	}

	public BigDecimal getDistanceRate() {
		return distanceRate;
	}

	public void setDistanceRate(BigDecimal distanceRate) {
		this.distanceRate = distanceRate;
	}

	public BigDecimal getCorrectLat() {
		return correctLat;
	}

	public void setCorrectLat(BigDecimal correctLat) {
		this.correctLat = correctLat;
	}

	public BigDecimal getCorrectLon() {
		return correctLon;
	}

	public void setCorrectLon(BigDecimal correctLon) {
		this.correctLon = correctLon;
	}
}
