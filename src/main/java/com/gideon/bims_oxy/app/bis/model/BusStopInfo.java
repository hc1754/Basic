package com.gideon.bims_oxy.app.bis.model;

import java.math.BigDecimal;

public class BusStopInfo {
	
	private String busStopId;
	private String busStopName;
	private String bsuStopSeq;
	private BigDecimal lat;
	private BigDecimal lon;
	private String favoriteYn;

	public String getBusStopId() {
		return busStopId;
	}

	public void setBusStopId(String busStopId) {
		this.busStopId = busStopId;
	}

	public String getBusStopName() {
		return busStopName;
	}

	public void setBusStopName(String busStopName) {
		this.busStopName = busStopName;
	}

	public String getBsuStopSeq() {
		return bsuStopSeq;
	}

	public void setBsuStopSeq(String bsuStopSeq) {
		this.bsuStopSeq = bsuStopSeq;
	}

	public BigDecimal getLat() {
		return lat;
	}

	public void setLat(BigDecimal lat) {
		this.lat = lat;
	}

	public BigDecimal getLon() {
		return lon;
	}

	public void setLon(BigDecimal lon) {
		this.lon = lon;
	}

	public String getFavoriteYn() {
		return favoriteYn;
	}

	public void setFavoriteYn(String favoriteYn) {
		this.favoriteYn = favoriteYn;
	}
}
